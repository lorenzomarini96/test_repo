"""Funzioni per socializzare."""



def hello(nome):
    """Funzione che dice ciao.

    Args:
        nome (str): Nome che viene salutato
    """
    print(f'Ciao {nome}')


def fuck(nome):
    """Funzione che manda a quel paese.

    Args:
        nome (str): Nome del tipo che detesti.
    """
    print(f'Fuck you {nome}')


